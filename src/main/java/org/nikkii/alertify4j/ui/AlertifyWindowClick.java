package org.nikkii.alertify4j.ui;

/**
 * Click handler for an {@link AlertifyWindow}.
 *
 * @author Nikki
 */
public interface AlertifyWindowClick {

	/**
	 * Called when a window is clicked.
	 *
	 * @param window The window that was clicked.
	 */
	public void alertClicked(AlertifyWindow window);
}
